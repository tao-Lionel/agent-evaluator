def _render_radar_chart(eval_avgs: dict[str, float], overall: float, t: dict) -> str:
    """Generate an SVG radar chart for capability dimensions."""
    import math

    if not eval_avgs or len(eval_avgs) < 3:
        return ""

    lang = "zh" if "评估" in t.get("title", "") else "en"
    labels_map = DIMENSION_LABELS.get(lang, DIMENSION_LABELS["en"])

    dimensions = list(eval_avgs.keys())
    values = [eval_avgs[d] for d in dimensions]
    labels = [labels_map.get(d, d) for d in dimensions]
    n = len(dimensions)

    cx, cy, r = 150, 150, 120
    angle_offset = -math.pi / 2  # start from top

    def polar(i, scale=1.0):
        angle = angle_offset + 2 * math.pi * i / n
        x = cx + r * scale * math.cos(angle)
        y = cy + r * scale * math.sin(angle)
        return x, y

    # Grid rings
    grid_svg = ""
    for level in [0.25, 0.5, 0.75, 1.0]:
        points = " ".join(f"{polar(i, level)[0]:.1f},{polar(i, level)[1]:.1f}" for i in range(n))
        grid_svg += f'<polygon points="{points}" fill="none" stroke="var(--border)" stroke-width="0.5"/>\n'

    # Axis lines
    axes_svg = ""
    for i in range(n):
        x, y = polar(i, 1.0)
        axes_svg += f'<line x1="{cx}" y1="{cy}" x2="{x:.1f}" y2="{y:.1f}" stroke="var(--border)" stroke-width="0.5"/>\n'

    # Data polygon
    data_points = " ".join(f"{polar(i, values[i])[0]:.1f},{polar(i, values[i])[1]:.1f}" for i in range(n))
    data_svg = f'<polygon points="{data_points}" fill="rgba(31,111,235,0.25)" stroke="var(--accent)" stroke-width="2"/>\n'

    # Data dots
    dots_svg = ""
    for i in range(n):
        x, y = polar(i, values[i])
        dots_svg += f'<circle cx="{x:.1f}" cy="{y:.1f}" r="3" fill="var(--accent)"/>\n'

    # Labels
    label_svg = ""
    for i in range(n):
        x, y = polar(i, 1.18)
        anchor = "middle"
        if x < cx - 10:
            anchor = "end"
        elif x > cx + 10:
            anchor = "start"
        label_svg += (
            f'<text x="{x:.1f}" y="{y:.1f}" text-anchor="{anchor}" '
            f'fill="var(--text2)" font-size="11">{escape(labels[i])} ({values[i]:.0%})</text>\n'
        )

    radar_title = {"en": "Capability Radar", "zh": "能力雷达图"}.get(lang, "Capability Radar")

    return (
        f'<div class="eval-section">'
        f'<h2>{radar_title}</h2>'
        f'<div style="text-align:center">'
        f'<svg viewBox="0 0 300 300" width="340" height="340" style="max-width:100%">'
        f'{grid_svg}{axes_svg}{data_svg}{dots_svg}{label_svg}'
        f'</svg></div></div>'
    )


def _render_task_card(result: dict, eval_names: list[str], t: dict) -> str:
    task_id = result.get("task_id", "unknown")
    task_info = result.get("task", {})
    desc = task_info.get("description", "")
    difficulty = task_info.get("difficulty", "")
    overall = result.get("overall_score", 0)
    terminated = result.get("terminated", "")
    steps = result.get("steps_taken", 0)
    elapsed = result.get("elapsed_seconds", 0)
    is_pass = overall >= 1.0 - 1e-6

    # Score bars
    score_bars = ""
    for name in eval_names:
        score = result.get("scores", {}).get(name, 0)
        score_bars += (
            f'<div class="score-row">'
            f'<span class="score-label">{escape(name)}</span>'
            f'<div class="score-track"><div class="score-fill {_score_color(score)}" '
            f'style="width:{score*100:.1f}%"></div></div>'
            f'<span class="score-value">{score:.2f}</span></div>\n'
        )

    # Trajectory
    traj_html = _render_trajectory(result.get("trajectory", []))

    status_class = "pass" if is_pass else "fail"
    status_text = t["pass"] if is_pass else t["fail"]

    return f"""
    <div class="task-card {status_class}">
      <div class="task-header">
        <span class="task-status {status_class}">{status_text}</span>
        <span class="task-id">{escape(task_id)}</span>
        <span class="task-diff">{escape(difficulty)}</span>
        <span class="task-meta">{steps} {t['steps']} &middot; {elapsed:.1f}s &middot; {escape(terminated)}</span>
        <span class="task-overall">{overall:.2f}</span>
      </div>
      <p class="task-desc">{escape(desc)}</p>
      <div class="scores-section">{score_bars}</div>
      <details class="trajectory-details">
        <summary>{t['conversation']}</summary>
        <div class="trajectory">{traj_html}</div>
      </details>
    </div>"""


_MSG_COLLAPSE_THRESHOLD = 300  # chars before collapsing


def _render_long_content(content: str, role_class: str, role_label: str, use_code: bool = False) -> str:
    """Render message content, collapsible if long, with copy button."""
    escaped = escape(content)
    length = len(content)

    if length <= _MSG_COLLAPSE_THRESHOLD:
        tag = "code" if use_code else "span"
        return (
            f'<div class="msg {role_class}"><span class="role">{role_label}</span>'
            f'<{tag}>{escaped}</{tag}></div>\n'
        )

    # Long content: show preview + expandable full content
    preview = escape(content[:200] + "...")
    msg_id = f"msg-{id(content) % 100000}"
    tag = "code" if use_code else "span"
    return (
        f'<div class="msg {role_class}">'
        f'<span class="role">{role_label}</span>'
        f'<span class="msg-meta">{length} chars</span>'
        f'<button class="copy-btn" onclick="navigator.clipboard.writeText('
        f'document.getElementById(\'{msg_id}-full\').textContent)">Copy</button>'
        f'<{tag} class="msg-preview" id="{msg_id}-preview">{preview}</{tag}>'
        f'<{tag} class="msg-full" id="{msg_id}-full" style="display:none">{escaped}</{tag}>'
        f'<button class="expand-btn" onclick="toggleMsg(\'{msg_id}\')">Expand</button>'
        f'</div>\n'
    )


def _render_trajectory(trajectory: list[dict]) -> str:
    html = ""
    for msg in trajectory:
        role = msg.get("role", "unknown")
        content = msg.get("content", "")
        if not isinstance(content, str):
            content = json.dumps(content, ensure_ascii=False, indent=2) if content else ""
        tool_calls = msg.get("tool_calls", [])
        tool_results = msg.get("tool_results", [])

        if role == "system":
            html += _render_long_content(content, "msg-system", "SYSTEM")
        elif role == "user":
            html += _render_long_content(content or "", "msg-user", "USER")
        elif role == "agent":
            if content:
                html += _render_long_content(content, "msg-agent", "AGENT")
            for tc in tool_calls:
                args = json.dumps(tc.get("arguments", {}), ensure_ascii=False, indent=2)
                call_text = f'{tc.get("name", "")}({args})'
                html += _render_long_content(call_text, "msg-tool-call", "TOOL CALL", use_code=True)
        elif role == "env":
            for tr in tool_results:
                output = tr.get("output", "")
                is_err = tr.get("error", False)
                cls = "msg-env-error" if is_err else "msg-env"
                html += _render_long_content(output, cls, "ENV", use_code=True)
    return html


def _score_color(score: float) -> str:
    if score >= 0.9:
        return "green"
    elif score >= 0.5:
        return "yellow"
    return "red"


