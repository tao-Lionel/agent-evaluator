def load_results(path: str) -> list[dict]:
    with open(path, "r", encoding="utf-8") as f:
        return json.load(f)


def generate_html(results: list[dict], source_path: str, lang: str = "en", analysis: str = "") -> str:
    t = LOCALES.get(lang, LOCALES["en"])

    # Extract metadata from results if embedded, and filter it out
    trial_stats = {}
    saturation_warning = ""
    if not analysis:
        for r in results:
            if "_analysis" in r:
                analysis = r["_analysis"]
            if "_trial_stats" in r:
                trial_stats = r["_trial_stats"]
            if "_saturation_warning" in r:
                saturation_warning = r["_saturation_warning"]
    results = [r for r in results if not any(k.startswith("_") for k in r)]

    total = len(results)
    passed = sum(1 for r in results if r.get("overall_score", 0) >= 1.0 - 1e-6)
    failed = total - passed
    avg_score = sum(r.get("overall_score", 0) for r in results) / total if total else 0

    # Collect evaluator names
    eval_names = set()
    for r in results:
        eval_names.update(r.get("scores", {}).keys())
    eval_names = sorted(eval_names)

    # Per-evaluator averages
    eval_avgs = {}
    for name in eval_names:
        scores = [r.get("scores", {}).get(name, 0) for r in results]
        eval_avgs[name] = sum(scores) / len(scores) if scores else 0

    # Difficulty breakdown
    by_diff: dict[str, list[dict]] = {}
    for r in results:
        diff = r.get("task", {}).get("difficulty", "unknown")
        by_diff.setdefault(diff, []).append(r)

    # Build task cards
    task_cards = "\n".join(_render_task_card(r, eval_names, t) for r in results)

    # Build evaluator chart bars
    eval_bars = "\n".join(
        f'<div class="eval-bar-row">'
        f'<span class="eval-bar-label">{escape(name)}</span>'
        f'<div class="eval-bar-track"><div class="eval-bar-fill" style="width:{avg*100:.1f}%">'
        f'{avg:.0%}</div></div></div>'
        for name, avg in eval_avgs.items()
    )

    # Difficulty breakdown rows
    diff_rows = "\n".join(
        f'<tr><td>{escape(diff)}</td>'
        f'<td>{len(rs)}</td>'
        f'<td>{sum(r.get("overall_score",0) for r in rs)/len(rs):.0%}</td></tr>'
        for diff, rs in sorted(by_diff.items())
    )

    # Build capability radar chart SVG
    radar_html = _render_radar_chart(eval_avgs, avg_score, t)

    # Build analysis section
    analysis_html = ""
    if analysis:
        analysis_html = (
            f'<div class="analysis-section">'
            f'<h2>{t["analysis"]}</h2>'
            f'<div class="analysis-content">{escape(analysis)}</div>'
            f'</div>'
        )

    # Build saturation warning HTML
    saturation_html = ""
    if saturation_warning:
        saturation_html = (
            f'<div class="analysis-section" style="border-left-color:var(--yellow)">'
            f'<h2>{t["saturation"]}</h2>'
            f'<div class="analysis-content">{escape(saturation_warning)}</div>'
            f'</div>'
        )

    # Build trial stats HTML
    consistency_html = ""
    if trial_stats:
        rows = ""
        for tid, st in trial_stats.items():
            k_cols = ""
            for key, val in st.items():
                if key in ("n", "c"):
                    continue
                if isinstance(val, float):
                    k_cols += f"<td>{val:.2f}</td>"
                else:
                    k_cols += f"<td>{val}</td>"
            rows += f"<tr><td>{escape(tid)}</td>{k_cols}</tr>\n"
        # Build header from first entry's keys
        first_st = next(iter(trial_stats.values()))
        headers = "".join(
            f"<th>{k}</th>" for k in first_st if k not in ("n", "c")
        )
        consistency_html = (
            f'<div class="eval-section">'
            f'<h2>{t["consistency"]}</h2>'
            f'<table class="diff-table"><tr><th>Task</th>{headers}</tr>'
            f'{rows}</table></div>'
        )

    return HTML_TEMPLATE.format(
        lang=lang,
        title=t["title"],
        source_label=t["source"],
        source=escape(source_path),
        total=total,
        passed=passed,
        failed=failed,
        avg_score=f"{avg_score:.0%}",
        label_total=t["total_tasks"],
        label_passed=t["passed"],
        label_failed=t["failed"],
        label_avg=t["avg_score"],
        label_eval=t["eval_breakdown"],
        label_diff=t["difficulty"],
        label_tasks=t["tasks"],
        label_avg_score=t["avg_score"],
        label_results=t["task_results"],
        footer=t["footer"],
        eval_bars=eval_bars,
        diff_rows=diff_rows,
        task_cards=task_cards,
        analysis_section=analysis_html,
        saturation_section=saturation_html,
        consistency_section=consistency_html,
        radar_section=radar_html,
    )


